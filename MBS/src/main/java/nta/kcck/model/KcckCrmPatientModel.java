package nta.kcck.model;

public class KcckCrmPatientModel {

	private String patient_name;
	private String disease_name;
	private String last_go_hospital;
	private String status_of_disease;
	private Integer patient_age;
	private String patient_sex;	
	private String patient_email;
	private String patient_tel;
	
	public String getPatient_name() {
		return patient_name;
	}
	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}
	public String getDisease_name() {
		return disease_name;
	}
	public void setDisease_name(String disease_name) {
		this.disease_name = disease_name;
	}
	public String getLast_go_hospital() {
		return last_go_hospital;
	}
	public void setLast_go_hospital(String last_go_hospital) {
		this.last_go_hospital = last_go_hospital;
	}
	public String getStatus_of_disease() {
		return status_of_disease;
	}
	public void setStatus_of_disease(String status_of_disease) {
		this.status_of_disease = status_of_disease;
	}
	public Integer getPatient_age() {
		return patient_age;
	}
	public void setPatient_age(Integer patient_age) {
		this.patient_age = patient_age;
	}
	public String getPatient_sex() {
		return patient_sex;
	}
	public void setPatient_sex(String patient_sex) {
		this.patient_sex = patient_sex;
	}
	public String getPatient_email() {
		return patient_email;
	}
	public void setPatient_email(String patient_email) {
		this.patient_email = patient_email;
	}
	public String getPatient_tel() {
		return patient_tel;
	}
	public void setPatient_tel(String patient_tel) {
		this.patient_tel = patient_tel;
	}
	
}
